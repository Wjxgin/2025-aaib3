///LeetCode學習計畫 大海撈針 在haystack找到needle
///28. Find the lndex of the First Occurrence in a String
class Solution {
public:
    int strStr(string haystack, string needle) {
        ///藥用迴圈，把所有可能的位置，都找過一次
        ///ex. sadbutsad 很難過，要找sad
        ///i:0 sad 把haystack剪前面N個字母
        ///     sad
        ///i:1  sad
        ///     abd
        ///i:2  sad ...
        ///     dbu
        int H = haystack.length(),N = needle.length();
        for(int i=0;i<=H-N;i++){///i試過所有位置
            if(haystack.substr(i,N) == needle)return i;
            ///從i開始，剪出N個字母[竟然相同] 找到答案!
        }
        return -1;///沒有找到答案，要回傳-1代表失敗
    }
};
